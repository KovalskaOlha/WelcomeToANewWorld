package class7;

public class ForLoops {
    public static void main(String[] args) {
        // prints numbers from 0 to 9
        int i=0;
        while(i<=9){
            System.out.println(i);
            i++;
        }
        System.out.println("-------------------------");

        for(int i1=0;i1<=9;i1++) {
            System.out.println(i1);
        }

        for( int num=1; num<=20; num+=2){
            System.out.println(num);
        }
        }

        }



package class7;

public class ForLoop4 {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            if(i%3!=0){
                System.out.print(i+" ");
            }

        }
        for (int i = 1; i < 20; i++) {
            if(i%3!=0){
                System.out.println(i);
            }

        }


    }
}

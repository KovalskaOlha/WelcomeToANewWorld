package class7;

public class ForLoops1 {
    public static void main(String[] args) {

        //initialization part, condition part, increment
        for (int i = 0; i < 20; i++) {  //i=i+2 or i+=2
            if (i % 2 != 0) {
                System.out.println(i);
            }
        }
    }
}
